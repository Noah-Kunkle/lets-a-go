    def color(self):
        for i in(range(len(self.lvl[0]))):
            for j in(range(len(self.lvl[1]))):
                if(self.lvl[i][j]==1):
                    self.lvl[i][j]=1,list(np.random.uniform(150,255,3))
                else:
                    self.lvl[i][j]=0,list(np.random.uniform(0,20,3))
        return(self.lvl)
